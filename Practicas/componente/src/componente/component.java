    public boolean showImage(int pos){
        boolean image_read = false;
        if(getClass().getResource((this.images_.get(pos))) != null && jLabel2.getIcon() == null){
            ImageIcon ii = new ImageIcon(getClass().getResource((this.images_.get(pos))));
            Image image = ii.getImage().getScaledInstance(jLabel2.getWidth(), jLabel2.getHeight(), Image.SCALE_SMOOTH);
            ii = new ImageIcon(image);
            jLabel2.setIcon(ii);
            image_read = true;
        }else if(jLabel2.getIcon() != null){
            jLabel2.setIcon(null);
            ImageIcon ii = new ImageIcon(getClass().getResource((this.images_.get(pos))));
            Image image = ii.getImage().getScaledInstance(jLabel2.getWidth(), jLabel2.getHeight(), Image.SCALE_SMOOTH);
            ii = new ImageIcon(image);
            jLabel2.setIcon(ii);
            image_read = true;
        }else{
            image_read = false;
        }
        return image_read;
    }
    
