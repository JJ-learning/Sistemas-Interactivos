    public void renderImages(){
        this.images_ = new ArrayList<String>();
        if(this.first == false){
            for(int i=0; i<=this.new_num_images; i++){
                String image = "/images/image-" + i + ".png";
                this.images_.add(image);
            }
        }else{
            for(int i=0; i<=this.num_images_; i++){
                String image = "/images/image-" + i + ".png";
                this.images_.add(image);
            }
        }
        this.actual_position_ = 0;
    }
    
    public void clearImages(){
        this.images_.clear();
        this.actual_position_ = 0;
        jLabel2.setIcon(null);
    }
    
    public void setFirstFalse(){
        this.first = false;
    }
    
    public boolean setNumImages(int images){
        images = images - 1;
        if(images < this.num_images_ && images >= 0){
            this.new_num_images = images;
            return true;
        }else{
            return false;
        }
    }
    public int getNumImages(){
        return this.new_num_images;
    }
  
    public int getPosition(){
        return this.actual_position_;
    }
    public boolean incrementPos(){
        boolean ismore = true;
        if(this.actual_position_ < this.num_images_){
            this.actual_position_++;
        }else{
            ismore = false;
        }
        return ismore;
    }
    public boolean decrementPos(){
        boolean ismore = true;
        if(this.actual_position_ > 0){
            this.actual_position_--;
        }else{
            ismore = false;
        }
        return ismore;
    }

    
