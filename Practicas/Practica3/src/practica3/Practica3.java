    private void PreviousMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PreviousMouseReleased
        // TODO add your handling code here:
        if(component1.getPosition() < 0){
            jLabel1.setText("There is no photos uploaded");
        }else if(component1.getPosition() == 0){
            jLabel1.setText("First photo");
        }else if(component1.decrementPos() == true && component1.getPosition() >= 0){
            component1.showImage(component1.getPosition());
            jLabel1.setText("Photo "+ (component1.getPosition()+1));
        }
    }//GEN-LAST:event_PreviousMouseReleased

    private void NextMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_NextMouseReleased
        // TODO add your handling code here:
        if(component1.getPosition() < 0){
            jLabel1.setText("There is no photos uploaded");
        }else if(component1.getPosition() == component1.getNumImages()){
            jLabel1.setText("Last photo");
        }else if(component1.incrementPos() == true && component1.getPosition() >= 0){
            component1.showImage(component1.getPosition());
            jLabel1.setText("Photo "+ (component1.getPosition()+1));
        }
    }//GEN-LAST:event_NextMouseReleased

    private void ReloadMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ReloadMouseReleased
        // TODO add your handling code here:
        component1.renderImages();
        if(component1.showImage(component1.getPosition()) == true){
            jLabel1.setText("Photo " + (component1.getPosition()+1));
        }else{
            jLabel1.setText("Error. Coudln't read image");
        }
    }//GEN-LAST:event_ReloadMouseReleased

    private void jToggleButton1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jToggleButton1ItemStateChanged
        // TODO add your handling code here:
        int state = evt.getStateChange();
        if(state == ItemEvent.SELECTED){
            ImageIcon ii = new ImageIcon(getClass().getResource("/images/reload.png"));
            Image image = ii.getImage().getScaledInstance(Reload.getWidth(), Reload.getHeight(), Image.SCALE_SMOOTH);
            ii = new ImageIcon(image);
            Reload.setIcon(ii);
            jLabel1.setText("Application turned on");
        }else if(state == ItemEvent.DESELECTED){
            component1.clearImages();
            jLabel1.setText("Application turned off");
        }
    }//GEN-LAST:event_jToggleButton1ItemStateChanged

    private void numImagesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_numImagesActionPerformed
        // TODO add your handling code here:
        
        component1.setFirstFalse();
        String text = numImages.getText();
        if(component1.setNumImages(Integer.parseInt(text)) == false){
            jLabel1.setText("Invalid number.");
        }else{
            jLabel1.setText("Correct number");
            component1.renderImages();
            component1.showImage(0);
        }
    }//GEN-LAST:event_numImagesActionPerformed

